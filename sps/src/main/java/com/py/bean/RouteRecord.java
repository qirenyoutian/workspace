package com.py.bean;

import java.util.List;

public class RouteRecord extends Route{
	private List<CameraRecord> cameraRecords;
	private BillingMethod billingMethod;
	
	public List<CameraRecord> getCameraRecords() {
		return cameraRecords;
	}
	public void setCameraRecords(List<CameraRecord> cameraRecords) {
		this.cameraRecords = cameraRecords;
	}
	public BillingMethod getBillingMethod() {
		return billingMethod;
	}
	public void setBillingMethod(BillingMethod billingMethod) {
		this.billingMethod = billingMethod;
	}
}
